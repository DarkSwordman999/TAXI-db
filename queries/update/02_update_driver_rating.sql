-- Изменение рейтинга водителя с кодом 5

\echo '=== ДО UPDATE ==='
SELECT код, фамилия, имя, рейтинг FROM ВОДИТЕЛИ WHERE код = 5;

UPDATE ВОДИТЕЛИ 
SET (рейтинг, стаж_лет) = (рейтинг + 0.2, стаж_лет + 1) 
WHERE код = 5;

