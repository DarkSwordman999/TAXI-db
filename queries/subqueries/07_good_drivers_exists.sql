\echo '=== ВОДИТЕЛИ, У КОТОРЫХ ВСЕ ПОЕЗДКИ ДОРОЖЕ 100 РУБ ==='

SELECT 
    В.фамилия || ' ' || В.имя AS водитель,
    COUNT(П.код_поездки) AS количество_поездок,
    ROUND(AVG(П.стоимость), 2) AS средняя_стоимость,
    MIN(П.стоимость) AS мин_стоимость,
    MAX(П.стоимость) AS макс_стоимость
FROM ВОДИТЕЛИ В
JOIN ПОЕЗДКИ П ON П.водитель = В.код
WHERE NOT EXISTS (
    SELECT 1 FROM ПОЕЗДКИ П2 
    WHERE П2.водитель = В.код AND П2.стоимость <= 100
)
GROUP BY В.фамилия, В.имя, В.код
ORDER BY средняя_стоимость DESC
LIMIT 20;
