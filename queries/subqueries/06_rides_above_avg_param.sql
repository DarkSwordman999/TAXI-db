\echo '=== ПОЕЗДКИ ДОРОЖЕ СРЕДНЕГО ДЛЯ ВОДИТЕЛЯ :arg1 ==='

SELECT 
    В.фамилия || ' ' || В.имя AS водитель,
    П.код_поездки,
    П.стоимость,
    (SELECT AVG(П2.стоимость) FROM ПОЕЗДКИ П2 WHERE П2.водитель = В.код) AS средний_по_водителю
FROM ПОЕЗДКИ П
JOIN ВОДИТЕЛИ В ON В.код = П.водитель
WHERE В.фамилия = :'arg1'
  AND П.стоимость > (SELECT AVG(П2.стоимость) FROM ПОЕЗДКИ П2 WHERE П2.водитель = В.код)
ORDER BY П.стоимость DESC;
