\echo '=== СТАТИСТИКА ПО КЛАССАМ АВТОМОБИЛЕЙ ==='

SELECT 
    класс,
    поездок,
    средняя_стоимость,
    общая_выручка
FROM (
    SELECT 
        А.класс,
        COUNT(*) AS поездок,
        ROUND(AVG(П.стоимость), 2) AS средняя_стоимость,
        SUM(П.стоимость) AS общая_выручка
    FROM ПОЕЗДКИ П
    JOIN АВТОМОБИЛИ А ON А.код = П.автомобиль
    GROUP BY А.класс
) AS class_stats
ORDER BY общая_выручка DESC;
