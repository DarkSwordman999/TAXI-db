-- Задача 4.3 (группа 4: подзапросы в компоненте УСЛОВИЯ)
-- Содержательная постановка: 
-- "Требуется определить водителей, у которых минимальная стоимость поездки 
--  выше средней стоимости всех поездок в таксопарке"
--
-- Используемые таблицы: ВОДИТЕЛИ, ПОЕЗДКИ
-- Роль подзапроса: возвращает общую среднюю стоимость (один столбец, одно значение)
-- для сравнения в HAVING MIN(...) > (подзапрос)

\echo '=== ВОДИТЕЛИ, У КОТОРЫХ МИНИМАЛЬНАЯ ПОЕЗДКА ВЫШЕ ОБЩЕЙ СРЕДНЕЙ ==='
\echo '(подзапрос в компоненте условия HAVING)'
\echo ''

SELECT 
    В.фамилия || ' ' || В.имя AS водитель,
    ROUND(AVG(П.стоимость), 2) AS средняя_стоимость_водителя,
    COUNT(П.код_поездки) AS количество_поездок,
    MIN(П.стоимость) AS мин_стоимость,
    MAX(П.стоимость) AS макс_стоимость
FROM ВОДИТЕЛИ В
JOIN ПОЕЗДКИ П ON П.водитель = В.код
GROUP BY В.фамилия, В.имя, В.код
HAVING MIN(П.стоимость) > (
    -- Подзапрос: вычисляет общую среднюю стоимость всех поездок
    SELECT AVG(стоимость) 
    FROM ПОЕЗДКИ
)
ORDER BY мин_стоимость DESC;
