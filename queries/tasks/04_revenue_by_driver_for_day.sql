-- ЗАДАЧА 4: Распределение объема выручки по водителям для дня недели, заданного параметром
-- Использование: psql -d taxi_lab5 -v arg1=1 -f queries/tasks/04_revenue_by_driver_for_day.sql
-- где arg1 = 1 (понедельник), 2 (вторник), ..., 7 (воскресенье)

\echo '=== ВЫРУЧКА ПО ВОДИТЕЛЯМ ДЛЯ ДНЯ НЕДЕЛИ :arg1 ==='

SELECT 
    ВОДИТЕЛИ.фамилия || ' ' || ВОДИТЕЛИ.имя AS "Водитель",
    SUM(ПОЕЗДКИ.стоимость) AS "Выручка"
FROM ПОЕЗДКИ
INNER JOIN ВОДИТЕЛИ ON ВОДИТЕЛИ.код = ПОЕЗДКИ.водитель
WHERE EXTRACT(DOW FROM ПОЕЗДКИ.дата) = (:arg1 % 7)
GROUP BY ВОДИТЕЛИ.фамилия, ВОДИТЕЛИ.имя
ORDER BY "Выручка" DESC;
